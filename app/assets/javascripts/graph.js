var vis = d3.select("#graph")
    .append("svg")

var w = 900.
var h = 400;
vis.attr("width" ,w)
    .attr("height", h);

vis.text("Neighbors Graph")
    .select("#graph")

var nodes = [{x: 30, y:50},
             {x: 50, y: 80},
             {x: 90, y: 120}]

var node = vis.selectAll("circle.node")
    .data(nodes)
    .enter().append("g")
    .attr("class", "node")

node.append("svg:circle")
    .attr("cx", function(d) { return d.x; })
    .attr("cy", function(d) { return d.y; })
    .attr("r", "10px")
    .attr("fill", "black");


vis.selectAll("circle.nodes")
    .data(nodes)
    .enter()
    .append("svg:circle")
    .attr("cx", function(d) { return d.x; })
    .attr("cy", function(d) { return d.y; })